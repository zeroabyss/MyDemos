package com.example.aiy.anime.Property;

/**
 * <p>功能简述：一个基本的point类，为了是说明TypeEvaluator
 * <p>Created by Aiy on 2017/7/31.
 */

public class Point {
    float x;
    float y;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
